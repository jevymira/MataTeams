namespace Teams.Domain.SharedKernel;

public enum Proficiency
{
    Interested,   // No prior experience or skills, but would like to learn.
    Beginner,     // Learning fundamentals. Requires guidance and support.
    Intermediate, // Comfortable with routine tasks and common problems.
    Proficient,   // Confident in most situations. Can handle complex tasks.
    Advanced      // Manages difficult challenges. Can mentor others.
}